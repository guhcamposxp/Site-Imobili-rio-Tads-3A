CREATE TABLE FILIAL (FILIAL_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
                    FILIAL_NOME VARCHAR(255),
                    FILIAL_CNPJ VARCHAR(255),
                    FILIAL_RUA VARCHAR(255),
                    FILIAL_NUMERO VARCHAR(255),
                    FILIAL_BAIRRO VARCHAR(255),
                    FILIAL_CEP VARCHAR(255),
                    FILIAL_ESTADO VARCHAR(255),
                    FILIAL_CIDADE VARCHAR(255))


